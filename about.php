    <h1>Jack Mattsson Foundation</h1>
    
    <h3>About Us</h3>
    <p>The Jack Mattsson Foundation was established in 2018 to honor our son/brother
Jack and his wishes after losing a battle against Osteosarcoma in 2017. Nobody
fought the fight better than he. So many people made a tremendous impact on
Jack and our family during the battle. Family, friends, community, and strangers all
were part of Jack’s Army and showed tremendous compassion and generosity during
Jack’s difficult times. We are forever grateful and aim to give back and pay it
forward to others who have trying times.</p>

    <h3>Our Mission</h3>
    <ul>
    	<li>Fund Cancer Research for Osteosarcoma</li>
    	<li>Provide Financial Support to patients and families</li>
    	<li>Support Anti-Bullying programs in schools</li>
    </ul>

